#include "libft.h"

int		ft_strnequ(const char *s, const char *t, size_t len)
{
	if (len == 0)
		return (1);
	while (*s == *t && *s && len)
	{
		s++;
		t++;
		len--;
	}
	return (len ? *s == *t : 1);
}
