#include "libft.h"

int		ft_atoi(const char *s)
{
	int		sign;
	long	new_n;
	long	n;

	sign = 1;
	n = 0;
	while (*s == ' ' || *s == '\t' || *s == '\n'
		|| *s == '\v' || *s == '\r' || *s == '\f')
		s++;
	if (*s == '-')
		sign = -1;
	if (*s == '-' || *s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
		if ((new_n = 10 * n + *s++ - '0') >= n)
			n = new_n;
		else
			return (sign == 1 ? -1 : 0);
	return ((int)(n * sign));
}
